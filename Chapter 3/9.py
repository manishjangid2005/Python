# Functions in python

# length
name = "James" # string type, name is a variable of the string type.
print(len(name))
# endswith
print(name.endswith("mes"))
print(name.startswith("Ja")) #this function is case sensitive j and J are different

# capitalize
print(name.capitalize()) # this will change the first letter of the string to uppercase, all other letters remain unchanged.

